var plus = function (left, right) {
    console.log(left + right);
};

var minus = function (left, right) {
    console.log(left - right);
};

var division = function (left, right) {
    console.log(left / right);
};

var multiple = function (left, right) {
    console.log(left * right);
};

function main () {
    plus(10, 5);
    minus(10, 5);
    division(10, 5);
    multiple(10, 5);
};